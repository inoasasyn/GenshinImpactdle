class Character:

    def __init__(self, name, rarity, element, weapon_type, region, model_type, special_stat,
                 ascension_local_speciality, ascension_boss_drop, ascension_mob_drop, release_date, version):
        self.name = name
        self.rarity = rarity
        self.element = element
        self.weapon_type = weapon_type
        self.region = region
        self.model_type = model_type
        self.special_stat = special_stat
        self.ascension_local_speciality = ascension_local_speciality
        self.ascension_boss_drop = ascension_boss_drop
        self.ascension_mob_drop = ascension_mob_drop
        self.release_date = release_date
        self.version = version
